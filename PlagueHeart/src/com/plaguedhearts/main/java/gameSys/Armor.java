package main.java.gamesys;

public class Armor {
    public static int armorRating;
    public static int armorVal;
    public static int armorBonusesMP;
    public static int armorBonusesHP;
    public static int armorBonusesST;
    public static int armorBonusesCH;

    public Armor (int armorRating, int armorVal, int armorBonusesMP, int armorBonusesHP, int armorBonusesST, int armorBonusesCH) {
            Armor.armorRating = armorRating;
            Armor.armorVal = armorVal;
            Armor.armorBonusesMP = armorBonusesHP;
            Armor.armorBonusesHP = armorBonusesHP;
            Armor.armorBonusesST = armorBonusesST;
            Armor.armorBonusesCH = armorBonusesCH;
    }
}
